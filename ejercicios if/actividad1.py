#actividad 1 con if-else
print("con este programa descubriremos si tu numero es negativo")
a = input ("ingresa tu numero : ")
numero = float(a)
if numero == 0:
    print("tu numero es neutro")
elif numero > 0:
    print("tu numero es positivo")
else : 
    print("tu numero es negativo")
print("gracias por utilizar este programa")

